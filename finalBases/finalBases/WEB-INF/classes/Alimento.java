public class Alimento{
	private int id;
	private String nombre;
	private double costo;

	public int getID(){
		return id;
	}
	public void setID(int a){
		id = a;
	}

	public String getNombre(){
		return nombre;
	}
	public void setNombre(String a){
		nombre=a;
	}

	public double getCosto(){
		return costo;
	}
	public void setCosto(double a){
		costo=a;
	}
}