public class Material{
	private int id;
	private String nombre;
	private int cantidad;

	public int getID(){
		return id;
	}
	public void setID(int a){
		id = a;
	}

	public String getNombre(){
		return nombre;
	}
	public void setNombre(String a){
		nombre=a;
	}

	public double getCantidad(){
		return cantidad;
	}
	public void setCantidad(int a){
		cantidad=a;
	}
}